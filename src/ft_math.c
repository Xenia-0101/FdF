#include "fdf.h"


void ft_point_matrix(t_point p, t_point *q, float R[3][3])
{
		q->x = R[0][0] * p.x + R[0][1] * p.y + R[0][2] * p.z;
		q->y = R[1][0] * p.x + R[1][1] * p.y + R[1][2] * p.z;
		q->z = R[2][0] * p.x + R[2][1] * p.y + R[2][2] * p.z;
}